package expertisesns.schooldataprocess;

public class UnivInfo 
{
	private String kindofUniv;
	private String nameofUniv;
	private String branchSchoolOrNot;
	private String establishment;
	private String address;
	
	public UnivInfo()
	{
		
	}

	public String getKindofUniv() {
		return kindofUniv;
	}

	public void setKindofUniv(String kindofUniv) {
		this.kindofUniv = kindofUniv;
	}

	public String getNameofUniv() {
		return nameofUniv;
	}

	public void setNameofUniv(String nameofUniv) {
		this.nameofUniv = nameofUniv;
	}

	public String getBranchSchoolOrNot() {
		return branchSchoolOrNot;
	}

	public void setBranchSchoolOrNot(String branchSchoolOrNot) {
		this.branchSchoolOrNot = branchSchoolOrNot;
	}

	public String getEstablishment() {
		return establishment;
	}

	public void setEstablishment(String establishment) {
		this.establishment = establishment;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}
}
